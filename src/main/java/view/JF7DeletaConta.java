    private void TableSelecionaClienteDeletaContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSelecionaClienteDeletaContaMouseClicked
        // TODO add your handling code here:
        TableSelecionaClienteDeletaConta.getValueAt(TableSelecionaClienteDeletaConta.getSelectedRow(), 0);
        for (int i = 0; i < ListaCliente.size(); i++) {
            if (TableSelecionaClienteDeletaConta.getValueAt(TableSelecionaClienteDeletaConta.getSelectedRow(), 0).equals(ListaCliente.get(i).getCPF())) {
                clienteDeletaConta = ListaCliente.get(i);
                CarregaTabelaConta();
                break;
            }
        }
        // TableSelecionaClienteDeletaConta.getSelectedRow();
    }//GEN-LAST:event_TableSelecionaClienteDeletaContaMouseClicked

    private void TableSelecionaContaDeletaContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSelecionaContaDeletaContaMouseClicked
        // TODO add your handling code here:
        nContaAtualiza = Integer.parseInt(TableSelecionaContaDeletaConta.getValueAt(TableSelecionaContaDeletaConta.getSelectedRow(), 1).toString());
        TableSelecionaContaDeletaConta.getValueAt(TableSelecionaContaDeletaConta.getSelectedRow(), 1);
        for (int i = 0; i < clienteDeletaConta.getContas().size(); i++) {
            if (nContaAtualiza == Contas.get(i).getNumero()) {
                ContaDeleta = clienteDeletaConta.getContas().get(i);
                break;
            }
        }
    }//GEN-LAST:event_TableSelecionaContaDeletaContaMouseClicked

