    public void CarregaTabelaCliente() {
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"CPF", "Nome", "Sexo", "Data de Nascimento", "Endereço"}, 0);
        for (int i = 0; i < ListaCliente.size(); i++) {
            modelo.addRow(new Object[]{ListaCliente.get(i).getCPF(), ListaCliente.get(i).getNome(), ListaCliente.get(i).getSexo(), ListaCliente.get(i).getDataNascimento(), ListaCliente.get(i).getEndereco()});
        }
        TableSelecionaClienteDeletaConta.setModel(modelo);
    }

    public void CarregaTabelaConta() {
        ArrayList<Conta> relatorioConta = clienteDeletaConta.getContas();
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Conta", "Numero", "Saldo"}, 0);
        for (int i = 0; i < relatorioConta.size(); i++) {
            modelo.addRow(new Object[]{relatorioConta.get(i).getTipo(),
                relatorioConta.get(i).getNumero(),
                relatorioConta.get(i).getSaldo()});
            TableSelecionaContaDeletaConta.setModel(modelo);
        }
    }

