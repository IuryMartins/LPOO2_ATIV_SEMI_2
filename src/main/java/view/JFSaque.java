    private void ButtonConfirmaSaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConfirmaSaqueActionPerformed
        // TODO add your handling code here:
        for(int i=0;i<Contas.size();i++){
            if(TextFieldInseirirNumContaSaque.getText().equals(Contas.get(i).getNumero())){
            
        //    TextFieldInserirValorSaque.getText(); Precisa ser numero para subtrair da conta 
            JOptionPane.showMessageDialog(this,"Saldo transferido com sucesso.");
            TextFieldInseirirNumContaSaque.setText("");
            TextFieldInserirValorSaque.setText("");
            }
                   else{
            JOptionPane.showMessageDialog(this,"Transferencia falhou");
            TextFieldInseirirNumContaSaque.setText("");
            TextFieldInserirValorSaque.setText("");
            
        }
        }
        
    }//GEN-LAST:event_ButtonConfirmaSaqueActionPerformed

